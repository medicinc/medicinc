for (const id of REQUIRED_MEDICAL_PROMOTION_COURSE_IDS) {
  if (COURSES.some((course) => course.id === id)) continue
  const legacyCourse = COURSES_LEGACY.find((course) => course.id === id)
  if (legacyCourse) {
    COURSES.push(legacyCourse)
  }
}

export function getCourseById(id) {
  return COURSES.find(c => c.id === id) || null
}
